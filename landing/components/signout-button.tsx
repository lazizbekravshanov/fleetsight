"use client";

import { signOut } from "next-auth/react";

export function SignOutButton() {
  return (
    <button
      onClick={() => signOut({ callbackUrl: "/login" })}
      className="rounded-lg border border-slate-700 px-3 py-2 text-sm text-slate-200"
    >
      Sign out
    </button>
  );
}
